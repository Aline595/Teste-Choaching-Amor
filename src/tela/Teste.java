    private void botao6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao6ActionPerformed
        // TODO add your handling code here:
        
        a = a + Integer.parseInt(resultadoA.getText());
        b = b + Integer.parseInt(resultadoB.getText());
        c = c + Integer.parseInt(resultadoC.getText());
        d = d + Integer.parseInt(resultadoC.getText());
        e = e + Integer.parseInt(resultadoC.getText());
        
        a = (a/75)*100;
        b = (b/75)*100;
        c = (c/75)*100;
        d = (d/75)*100;
        e = (e/75)*100;
        
        if(a>b && a>c && a>d && a>e){
            resultado_final.setText("Palavras de encorajamento.");
        }
        else if(b>a && b>c && b>d && b>e){
            resultado_final.setText("Atos de serviço.");
        }
        else if(c>a && c>b && c>d && c>e){
            resultado_final.setText("Presentes.");
        }
        else if(d>a && d>b && d>c && d>e){
            resultado_final.setText("Tempo de qualidade.");
        }
        else if(e>a && e>b && e>c && e>d){
            resultado_final.setText("Toque físico.");
        }
    }//GEN-LAST:event_botao6ActionPerformed

